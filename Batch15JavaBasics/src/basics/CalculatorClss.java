package basics;

public class CalculatorClss {
	
	public static int Add(int a, int b)
	{
		int res;
		res = a +b;
		
		return res;
	}
	
	public static int Sub(int a , int b)
	{
		return a-b;
	}
	
	public int Mult(int a , int b)
	{
		return a*b;
	}
	
	public double Div(int a, int b)
	{
		return a/b;
	}

}
